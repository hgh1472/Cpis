int	ft_str_is_alpha(char *str)
{
	int	i;
	int	re;

	i = 0;
	re = 1;
	while (*(str + i) != '\0')
	{
		re = 0;
		if (*(str + i) >= 'a' && *(str + i) <= 'z')
			re = 1;
		if (*(str + i) >= 'A' && *(str + i) <= 'Z')
			re = 1;
		if (re == 0)
			return (re);
		i++;
	}
	return (re);
}
