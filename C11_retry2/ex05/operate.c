int	plus(int num1, int num2)
{
	return (num1 + num2);
}

int	minus(int num1, int num2)
{
	return (num1 - num2);
}

int	mul(int num1, int num2)
{
	return (num1 * num2);
}

int	div(int num1, int num2)
{
	return (num1 / num2);
}

int	mod(int num1, int num2)
{
	return (num1 % num2);
}
