#include <unistd.h>

void ft_sort_int_tab(int *tab, int size)
{
	int tmp;

	for (int i = 0;i<size;i++)
	{
		for (int j = i+1;j<size;j++)
		{
			if (*(tab+i) >*(tab+j))
			{
				tmp = *(tab+i);
				*(tab+i) = *(tab+j);
				*(tab+j) = tmp;
			}
		}
	}
}

