void ft_rev_int_tab(int *tab, int size)
{
	int tmp;

	for (int i = 0;i<size/2;i++)
	{
		tmp = *(tab+i);
		*(tab+i) = *(tab+size-1-i);
		*(tab+size-1-i) = tmp;
	}
}
