#include <unistd.h>

void	print6(char a[], char b[], int n)
{
	write(1, a, 2);
	write(1, " ", 1);
	write(1, b, 2);
	if (n != 98)
		write(1, ", ", 2);
}

void	ft_print_comb2(void)
{
	int		n1;
	int		n2;
	char	a[2];
	char	b[2];

	n1 = 0;
	n2 = 1;
	while (n1 <= 98)
	{
		while (n2 <= 99)
		{
			a[0] = '0' + n1 / 10;
			a[1] = '0' + n1 % 10;
			b[0] = '0' + n2 / 10;
			b[1] = '0' + n2 % 10;
			if (n1 < n2)
			{
				print6(a, b, n1);
			}
			n2++;
		}
		n1++;
		n2 = n1 + 1;
	}
}
